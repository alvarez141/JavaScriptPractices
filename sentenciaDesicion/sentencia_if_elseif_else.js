// Sentencia if - else if else

let niNumero = 7;

// Revisar si el numero es positivo
if (niNumero > 0) {
    console.log(`el número ${niNumero} es positivo`);
} 
else if (niNumero < 0){
    console.log(`el número ${niNumero} es negativo`);   
}
else{
    console.log(`el número ${niNumero} es cero`);
}