//Operadores de asignación

let miNumero = 10;
console.log(miNumero);

miNumero = 20
console.log(miNumero);

//Operadores de asignación compuesta
// +=
miNumero += 5;
console.log(miNumero);

// -=
miNumero -= 3;
console.log(miNumero);

// *=
miNumero *= 2;
console.log(miNumero);

// /=
miNumero /= 4;
console.log(miNumero);

// %=
miNumero %= 2;
console.log(miNumero);

// **=
miNumero = 8
miNumero **= 2;
console.log(miNumero);