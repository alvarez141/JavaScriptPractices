// Operadores logicos

let a = true;
let b = false;

console.log(a); //true
console.log(b); //false

// Operador Logico && (and o Y)
//Regresa verdadero si ambos valores son verdaderos

console.log(`${a} && ${b} -> ${a && b}`); //true && false -> false


// Operador Logico || (or o O)
//Regresa verdadero si alguno de los valores es verdadero

console.log(`${a} || ${b} -> ${a || b}`); //true || false -> true


// Operador Logico ! (not o NO)
//Inviernte el valor iriginal , true por false y false por true
console.log(`${a} -> ${!a}`); //true -> false