// Realiza la suma de los primeros 5 numeros utilizando un ciclo for


// Ciclo for
let acumulador = 0;
for(i = 1; i <= 5; i++){
    acumulador += i;
    console.log(acumulador);
}

// ciclo while

let acumuilador2 = 0, number = 1;
while(number <= 5){
    acumuilador2 += number;
    number++;
    console.log(acumuilador2);
}

//ciclo do while
let acumulador3 = 0, number2 = 1;
do{
    acumulador3 += number2;
    number2++;
    console.log(acumulador3);
}while(number2 <= 5);